/* <Libraries> */
#include <stdio.h>

/* <main function> */
int
main()
  {
  int x, y;
  x=y=10;
  printf(" a = %d e b = %d\n", --x, y--);
  printf(" a = %d e b = %d\n", x, y);
  getchar();  
  return(0);
  }
